class ParallelSpeedup {
public:
	int numProcessors(int, int);
};

int ParallelSpeedup::numProcessors(int k, int over) {
	LL ret,cost=99999999999ll;
	for(LL proc=1;proc<=k;proc++){
		LL extr=(proc*(proc-1))/2;
		extr*=over;
		LL per=(k%proc==0)?k/proc:(k/proc+1);
		if(cost>per+extr)
			cost=per+extr,ret=proc;
	}
	return ret;
}
