class CircleMarket {
public:
	int makePurchases(vector <int>, vector <int>, int);
};

int CircleMarket::makePurchases(vector <int> op, vector <int> cl, int tr) {
	int ans=0,ti=0,pos=0;
	VB vis(SZ(op),false);

	while(ti<1000500){
		if(vis[pos]==false && op[pos]<=ti && ti<=cl[pos])
			++ans,vis[pos]=true;
		ti+=tr,pos=(pos+1)%(SZ(op));
	}
	return ans;
}
