class CutoffRounder {
public:
	int round(string, string);
};

int CutoffRounder::round(string num, string cutoff) {
	num.insert(num.begin(),'0');

	double orig=atof(num.c_str());
	double rnd=atof(cutoff.c_str());

	double rem=orig-int(orig);
	if(rem>=rnd)
		return int(orig+1);
	else
		return int(orig);
}
