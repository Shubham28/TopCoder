class MinimalDifference {
public:
	int findNumber(int, int, int);
};

int MinimalDifference::findNumber(int A, int B, int C) {

	int sumC=0,copy=C;
	while(copy!=0){
		sumC+=(copy%10);
		copy/=10;
	}
	
	int minim=99999,pos;
	EFOR(i,A,B){
		int dup=i,dsum=0,dif;
		while(dup!=0){
			dsum+=(dup%10);
			dup/=10;
		}
		dif=abs(dsum-sumC);
		if(dif==0)
			return i;
		else if(dif<minim)
			pos=i,minim=dif;
	}
	return pos;
}

