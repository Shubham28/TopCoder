class NumericalPerfectionLevel {
public:
	int getLevel(long long);
};

int NumericalPerfectionLevel::getLevel(long long N) {

	if(N==1)
		return 0;
		
	long long fac=0;
	for(long long i=2;i<=sqrt(N);i++){
		while(N%i==0){
			++fac;
			N=N/i;
		}
	}
	if(N>1)
		++fac;
		
	long ret=0;
	while( fac>=4 ){
		++ret;
		fac=fac/4;
	}
	return ret;	
}

