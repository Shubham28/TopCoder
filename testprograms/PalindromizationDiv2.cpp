class PalindromizationDiv2 {
public:
	int getMinimumCost(int);

	bool pal(int nm){
		if(nm<0)	return 0;

		int rev=0,cp=nm;
		while(cp!=0){
			rev=rev*10+(cp%10);
			cp/=10;
		}
		return (rev==nm);
	}
};

int PalindromizationDiv2::getMinimumCost(int X) {
	int inc=0;
	while(1){
		if(pal(X-inc) || pal(X+inc))		return inc;
		++inc;
	}
}
