class ReverseSums {
public:
	int getSum(int);
};

int ReverseSums::getSum(int N) {
	int copy=N,sum=0;
	
	while(copy!=0){
		sum=sum*10 + copy%10;
		copy/=10;
	}
	return sum+N;
	
}

