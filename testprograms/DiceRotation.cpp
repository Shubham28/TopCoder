int DiceRotation::theCount(int glx, int gly) {
	if(glx==0)
		return (gly%4==0);
	if(gly==0)
		return (glx%4==0);

	if(glx==1)
		return (gly==4)?2:0;
	if(gly==1)
		return (glx==4)?2:0;

	if(glx==4 || gly==4){
		int seq[]={1,2,5,6,12};
		if(glx==4)
			return (gly<=4)?seq[gly]:gly+3;
		if(gly==4)
			return (glx<=4)?seq[glx]:glx+3;
	}

	return 2;
}
