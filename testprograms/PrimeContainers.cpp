class PrimeContainers {
public:
	int containerSize(int);
	
	bool PrimeChk(int N)
	{
	if(N<=1)
		return false;
	if(N<=3)
		return true;
	if(N%2==0 || N%3==0)
		return false;
	if(N<=8)
		return true;

// All Prime Factors >=3 are of the form 6nï¿½1

	int fac=5;
	while(fac*fac<=N){

		if(N%fac==0)
			return false;
		if(N%(fac+2)==0)
			return false;
		fac+=6;
	}
	return true;
	}

};

int PrimeContainers::containerSize(int N) {
	int cnt=0;
	for(int i=1;N/i>=1;i*=2)
		if(PrimeChk(N/i))
		++cnt;
	return cnt;
}
