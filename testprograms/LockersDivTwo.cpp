class LockersDivTwo {
public:
	int lastOpened(int);
	int openLock(int tot,int inc) {
		if(tot==1)
			return 1;

		int ret=openLock(tot-(tot+inc-1)/inc,inc+1);
		return ret+1+(ret-1)/(inc-1);
	}
};

int LockersDivTwo::lastOpened(int N) {
	return openLock(N,2);
}
