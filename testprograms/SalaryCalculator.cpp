class SalaryCalculator {
public:
	double calcHours(int, int, int);
};

double SalaryCalculator::calcHours(int P1, int P2, int sal) {
	double ret=P1*200.0;
	if(ret>=sal)
		return sal/double(P1);
	
	sal-=ret;
	return 200+sal/double(P2);
}
