class NoOrderOfOperations {
public:
	int evaluate(string);
};

int NoOrderOfOperations::evaluate(string expr) {
	int result=(int)expr[0]-48;
	
	for(int i=2;i<expr.size();i=i+2){
		
		if( expr[i-1] == '+' )
			result=result + ( expr[i] -48);
		else if( expr[i-1] == '-' )
			result=result - ( expr[i] -48);
		else if( expr[i-1] == '*' )
			result=result * ( expr[i] -48);
		else if( expr[i-1] == '/' )	
			result=result / (expr[i] - 48);
	}
	return result;
}


