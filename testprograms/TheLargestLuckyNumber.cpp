class TheLargestLuckyNumber {
public:
	int find(int);
};

int TheLargestLuckyNumber::find(int n) {

	for(long i=n;i>=4;i--){
		long tmp=i;
		bool cond=true;
		while( tmp!=0 ){
			if( tmp%10==4 || tmp%10==7 ){
			} else{
				cond=false;
				break;
			}
			tmp/=10;
		}
		if( cond )
			return i;
	}
}


