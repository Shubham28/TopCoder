class MirroredClock {
public:
	string whatTimeIsIt(string);
};

string MirroredClock::whatTimeIsIt(string T) {

	int min=atoi(T.substr(3,2).c_str());
	min=60-min;
	if(min==60)
		min=0;

	int hr=atoi(T.substr(0,2).c_str());
	hr=11-hr;
	if(min==0)
		++hr;
	if(hr==12)
		hr=0;		

	char buff1[10],buff2[10];
	sprintf(buff1,"%d",hr);
	sprintf(buff2,"%d",min);
	
	string HR(buff1),MIN(buff2);
	if(HR.size()==1)
		HR="0"+HR;
	if(MIN.size()==1)
		MIN="0"+MIN;

	return HR+":"+MIN;
}
