class FormatAmt {
public:
	string amount(int, int);
};

string FormatAmt::amount(int dollars, int cents) {

	string temp="";
	while(cents!=0){
		int rem=cents%10;
		temp=(char)(rem+48) + temp;
		cents/=10;
	}
	if(temp.size()==1)
		temp="0"+temp;
	else if(temp.size()==0)
		temp="00";
	
	string dec="." + temp;
	string bin="";
	
	int count=0;
	while(dollars!=0){
	
		if(count%3==0 && count!=0)
			bin="," + bin;
				
		int rem=dollars%10;
		bin=(char)(rem+48) + bin;
		dollars/=10;
		++count;
	}
	if(bin.size()==0)
		bin="0";
	return ("$"+bin+dec);
}
	


