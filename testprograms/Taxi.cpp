class Taxi {
public:
	double maxDis(int, int, int);
};

double Taxi::maxDis(int maxX, int maxY, int taxiDis) {
	
	if( maxX+maxY < taxiDis )
		return -1;
	else {
		for(long i=taxiDis;i>=0;i--){
			if( max(i,taxiDis-i)<=max(maxX,maxY) )
				if( min(i,taxiDis-i)<=min(maxX,maxY) ){
					long long double ans=(i*i);				cout<<"ans = "<<ans<<endl;
					ans=ans+ (taxiDis-i)*(taxiDis-i);		cout<<"ans = "<<ans<<endl;
					long long double fin=sqrt(ans);			cout<<"fin= "<<fin<<endl;
					return fin;	}
		}
	}
}


